#!/usr/bin/env node
/**
 * Generate DIRAC subroutine wrappers for bash commands
 * 
 * This tool reads a command specification and generates clean DIRAC XML
 * wrappers that are easier for LLMs to understand and generate.
 * 
 * Usage:
 *   node bash-to-dirac.js commands.yml > lib/bash-commands.di
 */

import fs from 'fs';
import yaml from 'js-yaml';

// Command specification format:
// commands:
//   - name: ls
//     description: List directory contents
//     params:
//       - name: path
//         type: string
//         required: false
//         default: "."
//         description: Directory to list
//       - name: all
//         type: boolean
//         required: false
//         flag: -a
//         description: Show hidden files
//       - name: long
//         type: boolean
//         required: false
//         flag: -l
//         description: Long format

function generateSubroutine(cmd) {
  const lines = [];
  
  // Build parameter attributes
  const paramAttrs = cmd.params?.map(p => {
    const parts = [p.type || 'string'];
    if (p.required) parts.push('required');
    else parts.push('optional');
    if (p.description) parts.push(p.description);
    if (p.options) parts.push(p.options.join('|'));
    if (p.example) parts.push(p.example);
    return `  param-${p.name}="${parts.join(':')}"`;
  }) || [];
  
  lines.push(`<subroutine name="${cmd.name}"`);
  if (cmd.description) {
    lines.push(`  description="${cmd.description}"`);
  }
  lines.push(...paramAttrs);
  lines.push(`  meta-bash-command="${cmd.bashName || cmd.name}">`);
  lines.push('');
  
  // Build the system command
  const bashCmd = cmd.bashName || cmd.name;
  lines.push(`  <defvar name="cmd" value="${bashCmd}" />`);
  
  // Add boolean flags
  const boolParams = cmd.params?.filter(p => p.type === 'boolean') || [];
  for (const p of boolParams) {
    lines.push(`  <test-if test="$${p.name}" eq="true">`);
    lines.push(`    <assign name="cmd"><variable name="cmd" /> ${p.flag || '--' + p.name}</assign>`);
    lines.push('  </test-if>');
  }
  
  // Add string/value parameters
  const valueParams = cmd.params?.filter(p => p.type !== 'boolean') || [];
  for (const p of valueParams) {
    if (p.required) {
      if (p.flag) {
        lines.push(`  <assign name="cmd"><variable name="cmd" /> ${p.flag} <variable name="${p.name}" /></assign>`);
      } else {
        lines.push(`  <assign name="cmd"><variable name="cmd" /> <variable name="${p.name}" /></assign>`);
      }
    } else {
      lines.push(`  <test-if test="$${p.name}" ne="">`);
      if (p.flag) {
        lines.push(`    <assign name="cmd"><variable name="cmd" /> ${p.flag} <variable name="${p.name}" /></assign>`);
      } else {
        lines.push(`    <assign name="cmd"><variable name="cmd" /> <variable name="${p.name}" /></assign>`);
      }
      lines.push('  </test-if>');
    }
  }
  
  lines.push('');
  lines.push('  <system><variable name="cmd" /></system>');
  lines.push('</subroutine>');
  lines.push('');
  
  return lines.join('\n');
}

function generateLibrary(spec) {
  const lines = [];
  
  lines.push('<!-- Auto-generated DIRAC wrappers for bash commands -->');
  lines.push('<!-- Generated by bash-to-dirac.js -->');
  lines.push('<!-- Edit the YAML spec file and regenerate, do not edit this file directly -->');
  lines.push('');
  lines.push('<dirac>');
  lines.push('');
  
  for (const cmd of spec.commands) {
    lines.push(generateSubroutine(cmd));
  }
  
  lines.push('</dirac>');
  
  return lines.join('\n');
}

// Main
const specFile = process.argv[2] || 'bash-commands.yml';

if (!fs.existsSync(specFile)) {
  console.error(`Error: Specification file not found: ${specFile}`);
  console.error('');
  console.error('Usage: node bash-to-dirac.js <spec-file.yml>');
  console.error('');
  console.error('Example spec file format:');
  console.error(`
commands:
  - name: ls
    description: List directory contents
    params:
      - name: path
        type: string
        required: false
        description: Directory to list
      - name: all
        type: boolean
        flag: -a
        description: Show hidden files
`);
  process.exit(1);
}

const spec = yaml.load(fs.readFileSync(specFile, 'utf-8'));
const output = generateLibrary(spec);
console.log(output);
